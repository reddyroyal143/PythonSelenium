#Arithmetic Operator
a=5
b=2

print(a+b) #addition a,b
print(a-b) #subtraction
print(a*b) #multiply
print(a/b) #2.5
print(a//b) #2 quotient
print(a%b) #1 reminder
print(a**b) #exponential

#Relational Operators/Comparison Operators
#> < >= <= != ==
#This always returns a boolean value True/false
a=5
b=10
print(a>b) #False
print(a<b) #true
print(a==b) #false
print(a!=b) #true
print(a<=b) #True
print(a>=b) #False

#Logical Operators
# and or not (wors with boolean)
a=True
b=False

print(a and b) #false
print(a or b) #true
print(not a) #false



